#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	size_t	i;
	char	*str;

	str = (char *)s;
	i = -1;
	while (n > ++i)
		if (str[i] == (char)c)
			return (str + i);
	return (NULL);
}
