#include "libft.h"

int		ft_find_len(char *s, char c)
{
	unsigned int			nb;

	nb = 0;
	while (*s && *s == c)
		s++;
	while (*s && *(s + 1))
	{
		if (*s != c && *(s + 1) && *(s + 1) == c)
			nb++;
		s++;
	}
	if (*s && *s != c)
		++nb;
	return (nb);
}
